package it.pyrox.maze.model;

public class Cell {
	
	// TODO maybe coordinates are useless
	protected int row;
	protected int col;
	
	public Cell(int row, int col) {
		this.row = row;
		this.col = col;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
}
